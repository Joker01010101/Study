<?php
$x = 10;  
$y = 9;

// 加法
echo $x + $y;
// output:19

// 減法
echo $x - $y;
// output:1

// 乘法
echo $x * $y;
// output:90

// 除法
echo $x / $y;
// output:1.111111

// 餘數
echo $x % $y; 
// output:1

// x的y次方
echo $x %% $y;
// output:1000000000


// 變數加法
$x = 10;  
$x = $x + 30;
echo $x;
// output:40

// 變數減法
$x = 30;  
$x = $x - 10;
echo $x;
// output:20

// 變數除法
$x = 30;  
$x = $x * 10;
echo $x;
// output:3

// 變數除法餘數
$x = 33;  
$x = $x % 10;
echo $x;
// output:3
?>  